<template>
    <div class="load__wrapper">
        <div class="load__title">Обработка результатов</div>

        <div class="loader"></div>

        <div class="load__indicator">{{loadIndicator}}</div>
    </div>
</template>

<script>
import {ref, computed, onMounted, onUnmounted} from 'vue'
import { useRouter } from 'vue-router'
export default {
    name: 'QLoad',
    props: {
      quizKey: {
        type: Array
      }
    },
    setup(props) {
        const router = useRouter()
        const indicator = ref("Определение стиля мышления ")
        const timerId = ref(null)

        onMounted(() => {
            timerId.value = setInterval(() => {indicator.value += ".";}, 1000);
            startTimer()
        })

        onUnmounted(() => {
          clearInterval(timerId.value);
        })

        const loadIndicator = computed(() => {
            return indicator.value;
        })

        const startTimer = () => {
            setTimeout(() => {
                const quizKeyStr = JSON.stringify(props.quizKey);
                router.push(
                  {
                    name:'results',
                  })
            }, 10000)
        }

        return {
            router,
            indicator,
            loadIndicator,
        }
    },
}
</script>

